    private void addProdEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProdEnvioActionPerformed
        // TODO add your handling code here:
         double total = 0.0;
        for (int i = 0; i < modeloListaProductosEnv.getRowCount(); i++) {
            double precio = Double.parseDouble(modeloListaProductosEnv.getValueAt(i, 1).toString());
            total += precio;
        }
        
        // Mostrar el resultado en el JLabel precioLbl
        precioLbl.setText(String.valueOf(total));

    }//GEN-LAST:event_addProdEnvioActionPerformed

    private void confPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confPedidoActionPerformed
        // TODO add your handling code here:
        int distKm = Integer.parseInt(txtKm.getText());
        if(boxRepartidor.getSelectedItem().toString() == "Repartidor 1"){
            listaRep[0] = distKm;
          
        } else if (boxRepartidor.getSelectedItem().toString() == "Repartidor 2"){
            listaRep[1] = distKm;
           
        } else if (boxRepartidor.getSelectedItem().toString() == "Repartidor 3"){
            listaRep[2] = distKm;
            
        }
        

        txtKm.setText("");
        precioLbl.setText("");
        System.out.println("Producto Agregado");
        int recorrido = Integer.parseInt(txtKm.getText());
        String repartidor = boxRepartidor.getSelectedItem().toString();
        int monto =  Integer.parseInt(precioLbl.getText());
         momentoPedido = LocalDateTime.now();


        Pedido cantiPedidos = new Pedido (repartidor, recorrido, monto, momentoPedido);
        int contadorPedido = historialPedidos.contadorPedido;

        historialPedidos.cantPedidos[contadorPedido] = cantiPedidos;

        historialPedidos.contadorPedido++;
        
        System.out.println("Agregado");
        
    }//GEN-LAST:event_confPedidoActionPerformed

