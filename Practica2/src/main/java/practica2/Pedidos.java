    private double calcularTotal() {
    double total = 0.0;
    for (int i = 0; i < modeloListaProductosEnv.getRowCount(); i++) {
        double precio = Double.parseDouble(modeloListaProductosEnv.getValueAt(i, 1).toString());
        total += precio;
    }
    return total;
}
    

    
    
